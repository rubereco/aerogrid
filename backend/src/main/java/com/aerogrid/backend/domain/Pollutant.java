package com.aerogrid.backend.domain;

/**
 * Enum representing types of air pollutants that can be measured.
 *
 * @author AeroGrid
 * @version 1.0
 * @since 2026-01-21
 */
public enum Pollutant {
    /** Nitrogen Dioxide */
    NO2,
    /** Particulate Matter 10 micrometers or less */
    PM10,
    /** Particulate Matter 2.5 micrometers or less */
    PM25,
    /** Ozone */
    O3,
    /** Carbon Monoxide */
    CO,
    /** Sulfur Dioxide */
    SO2,
    H2S,
    C6H6,
    PM1
}
